                                                        <li>
                                                            <p>The niche is the place where the Imam prays in the direction
                                                                of the qiblah. There have been many
                                                                niches in the Prophet’s Mosque throughout history.</p>
                                                        </li>
                                                        <li>
                                                            <p>After shifting the qiblah to the Sacred House in Makkah, the
                                                                Prophet, (blessings and peace be
                                                                upon him), changed the niche from the northern to the
                                                                southern part of the mosque. Then, it was
                                                                moved to the southernmost end of the expansion that took
                                                                place during the era of Omar bin
