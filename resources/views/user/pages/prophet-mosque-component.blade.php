                                                <h5 class="fnoUYi">Sections of the Prophet's Mosque</h5>
                                                <div class="description-text" style="line-height: 1.4rem;">
														<p>The Prophet’s Mosque has a long history of construction that began
                                                        during the time of the Prophet himself, blessings and peace be upon
                                                        him, and during the period of his honorable companions, may Allah be
                                                        pleased with them. Muslim rulers successively expanded and cared for
                                                        the mosque following the example of the Messenger, blessings and
                                                        peace be upon him. The Prophet’s Mosque consists of:</p>
														<p><strong>The Old Mosque:</strong> It is the mosque where the Prophet,
                                                        blessings and peace be upon him, prayed. Today, domes decorated with
                                                        Quranic verses are erected over it, and its pillars are topped with
                                                        golden crowns.</p>
														<p><strong>Gravel-covered areas:</strong> These are two open areas immediately after the old mosque. They were previously covered with gravel.</p>
														<p><strong>The Honorable Rawdah:</strong> The honorable Rawdah is part of the Prophet’s Mosque. It is located at the front of the mosque and extends from the house of the Messenger of Allah, blessings and peace be upon him, (the honorable burial chamber) to his pulpit.</p>														
														<p><strong>Expansions:</strong> The Prophet's Mosque was expanded more than once by the kings of Saudi Arabia. Expansions comprise the other mosque buildings surrounding the gravel-covered areas.</p>
														<p><strong>Women's Prayer halls:</strong> There are two large prayer areas for women; namely, the western and northern prayer halls.</p>

														<p><strong>Outer Courtyards:</strong> These are large prayer areas, covered with canopies to protect worshipers from the sun.</p>
