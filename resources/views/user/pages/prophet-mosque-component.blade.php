                                                                during the era of Othman bin Affan.
                                                                This is the place where the Imam stands today.</p>
                                                        </li>
                                                    </ul>
                                                    <p><strong>Niches of the Prophet's Mosque</strong>
                                                        Numerous niches have been built in the Prophet’s Mosque throughout
                                                        the ages. The most important of those
                                                        niches are:</p>
                                                    <ul>
                                                        <li><strong>The Prophet’s niche</strong>: It is located in the
                                                            honorable Rawdah, to the left of the
                                                            pulpit, where the Imam prays today.</li>
                                                        <li><strong>The Ottoman niche</strong>: It is located in the
                                                            southern wall of the Mosque where the imam
                                                            used to pray in the past.</li>
                                                    </ul>
