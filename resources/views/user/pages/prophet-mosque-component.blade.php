                                                        The Messenger of Allah, blessings and peace be upon him, fell ill,
                                                        died and was buried in Aisha's room.
                                                        The Prophet, blessings and peace be upon him, had said: “Every
                                                        prophet is buried where he died.” Then
                                                        his two companions, Abu Bakr and Omar, may Allah be pleased with
                                                        them both, were buried there.</p>
