                                                            One of the most prominent landmarks of Mina is the Jamarat
                                                            or the Jamrah Pillars that are thrown at the Jamarat Bridge.
                                                            The three Jamarat are located in Mina Valley. Currently,
                                                            they are within the Jamarat Bridge, which is one of the
                                                            great achievements of the Kingdom’s Government, in order to
                                                            organize crowds and provide safety when they go to throw
                                                            Jamarat. At the beginning of the Day of Nahr, only the seven
                                                            pebbles of the biggest Jamrah are thrown. During the days of
                                                            Tashreeq, each Jamrah is thrown at with seven pebbles, from
                                                            the Smallest Jamrah, the Middle Jamrah, and finally the
                                                            Biggest Jamrah. A pilgrim can throw Jamarat from any floor
                                                            of the Jamarat Bridge. It is very important for pilgrims to
                                                            abide by the schedule of Tafweej (Group Dispatching) set by
                                                            the organizers for throwing Jamarat and the directions of
                                                            movement, in order to maintain the safety and security of
                                                            others. The Jamarat Bridge is one of the pioneering projects
                                                            that has made a civilizational and engineering leap in the
                                                            service of pilgrims. The Jamarat Facility consists of four
                                                            floors, in addition to the ground floor. It is 80 meters
                                                            wide and has 12 entrances for Jamarat and 12 exits in the
                                                            four directions, which reduces overcrowding. The Bridge is
                                                            linked to the pilgrim camps.</p>
