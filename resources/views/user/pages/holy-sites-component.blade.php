                                                <div class="content">
                                                    <div class="description-text">
                                                        <p class="Description"
                                                            style="cursor: auto; translate: none; rotate: none; scale: none; filter: blur(0px); opacity: 1; visibility: inherit; transform: translate(0px);">
                                                            A mosque that was built in Arafah where the Prophet, may
                                                            peace and prayers be upon him, delivered a sermon for the
                                                            last farewell Hajj. In Namirah Mosque, thousands of pilgrims
                                                            pray zuhr and asr prayers on the day of Arafah. It is
                                                            located to the west of Arafah. The western part of the
                                                            Mosque is located in Wadi ‘Arnah, which is a valley of
                                                            Makkah Al-Mukarramah where the Prophet prohibited pilgrims
                                                            from standing there because it is not a part of Arafat, but
                                                            it is very close thereto.
                                                            Namirah Mosque is known in many books by several other
                                                            names, such as the Mosque of the Prophet Ibrahim, Arafah
                                                            Mosque, and Arnah Mosque. The name Arafah Mosque was
                                                            originated from a village outside Arafah Area where the
                                                            Prophet, may peace and prayers be upon him, resided and then
                                                            moved to the valley, where he prayed zuhr and asr prayers
                                                            and delivered a sermon in the Mosque.
                                                            During the era of the Saudi State, the Mosque witnessed the
                                                            largest expansions in history, where it became the second
                                                            largest mosque in Makkah in terms of area after the Holy
                                                            Mosque. The length of the Mosque from east to west is 340
                                                            meters, and the width from north to south is 240 meters. Its
                                                            area is more than 110,000 square meters. Behind the Mosque,
                                                            there is a shaded area of 8 thousand square meters. The
                                                            Mosque accommodates about 350 thousand worshippers. It has
                                                            six minarets and an equipped outside broadcasting room for
                                                            broadcasting the sermon and zuhr and asr prayers on the day
                                                            of Arafat, directly by satellite.</p>
