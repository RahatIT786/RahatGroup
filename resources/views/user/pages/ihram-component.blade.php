                                                    <li>For men, it is Sunnah to raise their voices when reciting
                                                        Talbiyah. The
                                                        Prophet (blessing and peace be upon him) said: 'Jibreel came to
                                                        me and
                                                        ordered me to order my Companions to raise their voices when
                                                        reciting
                                                        Talbiyah.</li>
                                                    <li>It is prescribed for women to lower their voices when reciting
                                                        Talbiyah.
                                                    </li>
                                                </ul>
