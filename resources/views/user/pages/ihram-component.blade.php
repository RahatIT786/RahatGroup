                                                <p class="sc-2fe98234-7 iebARE">The problem of intertrigo and body fold
                                                    rush is
                                                    common among pilgrims performing Hajj and Umrah, especially among
                                                    people with
                                                    obesity and diabetes. The skin becomes infected due to friction and
                                                    lack of
                                                    underwear. Rush occurs in folds where two layers of the skin meet,
                                                    such as:</p>
                                                <ul>
                                                    <li>The groins</li>
                                                    <li>Armpits</li>
                                                    <li>Under the breasts</li>
                                                </ul>
                                                <p class="sc-2fe98234-7 iebARE">This is sometimes accompanied by
                                                    painful itching.
                                                </p>
                                                <p class="sc-2fe98234-7 iebARE"><strong>Prevention</strong></p>
                                                <ul>
                                                    <li>Take care of your personal hygiene</li>
                                                    <li>Keep body folds such as thighs and armpits well ventilated</li>
                                                    <li>Apply ointments on the body before walking</li>
                                                    <li>When infected, wash the area well before applying medications
                                                        such as
                                                        healing ointments</li>
                                                </ul>
                                                <p class="sc-2fe98234-7 iebARE"><strong>Precautions</strong></p>
                                                <ul>
                                                    <li>Wearing clean clothes will prevent many health problems such as
                                                        irritating
                                                        skin abrasions</li>
                                                    <li>Diabetics should be aware that peeling of the skin may increase
                                                        due to
