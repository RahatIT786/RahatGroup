                                                    circumstances that may prevent them from completing the rituals of
                                                    Hajj or Umrah.</p>
                                                <p class="iebARE">If a pilgrim fears that he will not be able to
                                                    complete the rituals of Hajj or Umrah for any reason, it is lawful
                                                    to stipulate a condition while in Ihram.</p>
                                                <p class="sc-2fe98234-7 iebARE"><strong>How to stipulate?</strong></p>
                                                <p class="iebARE">If the pilgrim performing Hajj or Umrah fears that he
                                                    will not be able to complete his rituals due to a disease or a
                                                    lawful excuse or for a reason beyond his control, it is allowed for
                                                    him/her to make exceptions in their utterance of the intention. One
                                                    does so by saying after saying "Labbayka Umrah" or "Labbayka
                                                    Hajjan": "If I'm deterred for any reason, then my place is where You
                                                    deterred me (this can be said in any language)". If a pilgrim says
                                                    this and stipulates a condition, and then something prevents them
                                                    from completing the ritual, it is permissible for him to leave the
                                                    state of Ihram without falling under any further obligation.</p>
                                                <p class="sc-2fe98234-7 iebARE"><strong>Who can stipulate a
                                                        condition?</strong></p>
                                                <p class="iebARE">Whoever fears that one will not complete the rituals
                                                    for a reason beyond their control, including:</p>
