                                                <ul>
                                                    <li>Ihram is entering into the act of worship of Hajj or Umrah. It
                                                        involves intending to enter into Umrah or Hajj and reciting
                                                        Talbiyah, which is the first of the pillars of Hajj and Umrah.
                                                    </li>
