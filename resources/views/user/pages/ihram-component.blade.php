                                                        heart with which you engage in a great act of worship. It is a
                                                        great act of worship for which Allah commanded us to make our
                                                        intentions sincere, and perform its rituals for His sake, Glory
                                                        be to Him, in the most perfect manner. As the Almighty said:
                                                        {Perform Hajj and Umrah for (the sake of) Allah}.</li>
                                                    <li>With this intention, things that are permissible before entering
                                                        into the state of Ihram are now forbidden: such as intercourse,
                                                        shaving the head, clipping nails, covering the head, wearing
