                                                    After entering the state of Ihram, they proceed to perform the
                                                    rituals in humility to Allah and supplicating Him alone.</p>
                                                <p class="iebARE">Ihram is the commencement of the rituals of Hajj or
                                                    Umrah. The rituals are commenced by intending to start Umrah or Hajj
                                                    and reciting Talbiyah. It is the first pillar of Hajj and Umrah.</p>
