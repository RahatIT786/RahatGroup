                                                        <ul>
                                                            <li>
                                                                <p>It is the mosque which witnessed the change of the
                                                                    qiblah direction.</p>
                                                            </li>
                                                            <li>
                                                                <p>When the Messenger of Allah, (blessings and peace be
                                                                    upon him), was in Makkah, he used to turn towards
                                                                    Jerusalem in his prayers, with the Ka'bah in front
                                                                    of him, between him and Jerusalem.</p>
                                                            </li>
                                                            <li>
                                                                <p>When he migrated to Madina, he kept praying toward
                                                                    Jerusalem for a year, while he liked to turn towards
                                                                    the Ka'bah. Ultimately, the following ayah was
                                                                    revealed to him {We have certainly seen the turning
                                                                    of your face, [O Muhammad], toward the heaven, and
                                                                    We will surely turn you to a qiblah with which you
                                                                    will be pleased. So, turn your face [i.e., yourself]
                                                                    toward Al-Masjid Al-Haram}. (2: 144). Immediately,
                                                                    the Prophet turned towards the Ka'bah.</p>
                                                            </li>
                                                            <li>
                                                                <p>It is reported that the change of the qiblah
                                                                    direction occurred when the Messenger of Allah,
                                                                    (blessings and peace be upon him), was in Bani
                                                                    Salamah neighborhood. He had already prayed two
                                                                    rak'ahs of Zuhr when the change of the Qiblah
                                                                    direction was revealed to him. He immediately turned
                                                                    towards the Ka'bah without interrupting the prayer
                                                                    and moved ahead of the congregation from the other
                                                                    direction. So, the Mosque was called Al-Qiblatain
                                                                    Mosque.</p>
                                                            </li>
