                                                        <p>It is a small mountain next to Mount Uhud.</p>
                                                        <p><strong>Origin of the Name</strong></p>
                                                        <ul>
                                                            <li>It was given this name because the Prophet, (blessings
                                                                and peace be upon him), ordered the archers in the
                                                                Battle of Uhud to take their positions at the top of the
                                                                mountain and to stay firm to protect the Muslim army and
                                                                prevent the enemy from turning around the mountain and
                                                                besieging the Muslims.</li>
                                                        </ul>
                                                        <p>There were 50 archers under the command of Abdullah bin
                                                            Jubair, (may Allah be pleased with him). The inevitable came
                                                            to pass as some archers disobeyed the Prophet's commands
                                                            when they saw that the polytheists had initially been
                                                            defeated and started to flee the battlefield. Then, the
                                                            archers left their positions and went down to collect booty
                                                            while their commander, bin Jubair, tried in vain to dissuade
                                                            them.</p>
                                                        <p>Therefore, Khaled bin Al-Walid, still a polytheist at the
                                                            time, took advantage of that mistake, turned around the
                                                            mountain with some elements of the polytheist army and
                                                            attacked the Muslim army. He managed to reverse the outcome
                                                            of the battle, and the victory of the Muslims turned into a
                                                            defeat in which many of the Prophet's companions, (may Allah
                                                            be pleased with them), were killed.</p>
