                                                <p class="sc-2fe98234-7 iebARE"><strong>What are Safa and
                                                        Marwa?</strong></p>
                                                <ul>
                                                    <li>Safa and Marwa are two small hills on which Ismail's mother,
                                                        Hagar, climbed in search of food or water.</li>
                                                </ul>
                                                <p class="sc-2fe98234-7 iebARE"><strong>Safa</strong></p>
                                                <ul>
                                                    <li>It is a small hill from which Sa'i begins. The rocks of the
                                                        hill are still visible on the ground floor there.</li>
                                                </ul>
                                                <p class="sc-2fe98234-7 iebARE"><strong>Marwa</strong></p>
                                                <ul>
                                                    <li>It is a small hill where the round of Sa'i ends.</li>
                                                </ul>
                                                <p class="sc-2fe98234-7 iebARE"><strong>Sa'i is an important
                                                        rite</strong></p>
                                                <ul>
                                                    <li>Performing Sa'i between Safa and Marwa is a ritual of Hajj
                                                        and Umrah alike. Allah the Exalted said: {Indeed, Safa and
                                                        Marwa are among the symbols of Allah. So whoever makes Hajj
                                                        to the House or performs Umrah - there is no blame upon him
                                                        for walking between them}.</li>
                                                    <li>The pilgrim performing Hajj or Umrah worships his Lord by
                                                        crossing the distance between Safa and Marwa seven times, as
                                                        did his Noble Messenger (blessings and peace be upon him).
                                                    </li>
                                                </ul>
                                                <p class="sc-2fe98234-7 iebARE"><strong>The beginning and end of
                                                        Sa'i</strong></p>
                                                <ul>
                                                    <li>You start your Sa'i from Safa, heading towards Marwa. Upon
                                                        reaching Marwa, you have completed one round. Crossing the
                                                        distance back to Safa fulfills the second round, and so on,
                                                        until the seventh-round ends at Marwa.</li>
                                                </ul>
                                                <p class="sc-2fe98234-7 iebARE"><strong>Supplication at the
                                                        beginning of Safa and Marwa</strong></p>
                                                <ul>
                                                    <li>When you arrive at the beginning of Safa and Marwa, it is
                                                        Sunnah to raise your hands, face the Qiblah and supplicate
                                                        to Allah, saying: “Allahu Akbar” before starting a new
                                                        round.</li>
                                                </ul>
                                                <p class="sc-2fe98234-7 iebARE"><strong>The two green
                                                        signs</strong></p>
                                                <ul>
                                                    <li>It is recommended for capable men to run between the two
                                                        green signs, following the example of the Messenger of Allah
                                                        (blessings and peace be upon him).</li>
                                                </ul>
                                                <p class="sc-2fe98234-7 iebARE"><strong>Supplication during
                                                        Sa'i</strong></p>
                                                <ul>
                                                    <li>A pilgrim should fill the entire time of his Sa'i with
                                                        remembrance and supplication to Allah or with reciting the
                                                        Quran.</li>
                                                </ul>
                                                <p class="sc-2fe98234-7 iebARE"><strong>The Prophet's supplication
                                                        during Sa'i</strong></p>
                                                <ul>
                                                    <li>When the Messenger of Allah (blessings and peace be upon
                                                        him) first reached Safa after the circumambulation, he would
                                                        recite this part of the Quranic Ayah {Indeed, Safa and Marwa
                                                        are among the symbols of Allah}, and would add: {Begin with
                                                        what Allah began with}.</li>
                                                    <li>When climbing up Safa and Marwa, the Prophet (blessings and
                                                        peace be upon him) used to face the Qiblah and recite:
                                                        "Allah is the greatest. Allah is the greatest. There is no
                                                        true god but Allah alone. He has no partner. His is the
                                                        Sovereignty, to Him praise is due and He is powerful over
                                                        everything. There is no true god but Allah alone. He
                                                        fulfilled His promise, helped His servant to victory, and
                                                        alone routed the Confederates". After that he shall
                                                        supplicate with whatever he wishes, repeat the above
                                                        remembrance, and proceed with Sa'i.</li>
                                                </ul>
                                                <p class="sc-2fe98234-7 iebARE">The person performing Sa'i should
                                                    not stop to say prayers in a place if that disrupts the flow of
                                                    pedestrians and causes crowding. He must adhere to the
                                                    instructions of security personnel in the sanctuary.</p>
