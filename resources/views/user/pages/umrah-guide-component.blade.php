            <div class="mainheading topmargin30">
                <h5>UMRAH RITUALS:</h5>
            </div>
            <div class="infotxt">The pilgrim performs a series of ritual acts symbolic of the lives
                of&nbsp;Ibrahim&nbsp;and
                his second wife&nbsp;Hajar, and of solidarity with Muslims worldwide. Pilgrims enter the perimeter of
                Mecca
                in a state of&nbsp;Ihram&nbsp;and perform Umrah.</div>

            <div class="mainheading topmargin30">
                <h5>Pillars of Umrah:</h5>
            </div>
            <div class="infotxt">Ihram,Tawaf,Sa'I,Halq&nbsp;or&nbsp;taqsir,</div>

            <div class="mainheading topmargin30">
                <h5>FIRST PILLAR : IHRAM,</h5>
            </div>
            <div class="infotxt">
                <ul>
                    <li><strong>A sacred state (of mind and body) into which a Muslim enters before performing Hajj or
                            ’Umrah.</strong></li>
                    <li>Cleaning your body (removing hair, clipping nails, trimming moustache, taking a shower) is
                        recommended. This can be done before flying.</li>
                    <li>Wear your Ihram clothes before arriving at the Miqat</li>
                    <li>For men, this is two sheets of white clothing material.</li>
                    <li>For ladies, ordinary humble clothing will suffice.</li>
                    <li>It is recommended to put on the Ihram at the air terminal before flight.</li>
                    <li>Make your intention to perform Umrah.&nbsp;<strong>(Niyyah)</strong></li>
                    <li>Say&nbsp;Allahumma labbayka ’Umrah&nbsp;</li>
                    <li>Recite the&nbsp;Talbiyah.</li>
                </ul>
                <br>
                If you forget to enter into Ihram at the Miqat , you &nbsp;must either exit Makkah and re-enter it, or
                sacrifice a sheep as a penalty.<br>
                <br>
                <strong>Miqat:</strong>&nbsp;it is a location outside of Makkah where Ihram will begin.<br>
                <br>
                <strong>Mawaqit:</strong>&nbsp;Fixed Times and Places For Ihram<br>
                <br>
                Ibn 'Abbas (RAA) narrated that the Messenger of Allah (ﷺ) specified for the people of Madinah,
                DhulHulaifah
                (a place 540 km to the north of Makkah) as miqat. For those coming from ash-Sham (including Syria,
                Jordan
                and Palestine), he specified al-Juhfah (a place 187 km to the north-west of Makkah and close to Rabigh,
                where they now perform their Ihram). For those coming from Najd, he specified Qran al-Manazil, (a
                mountain,
                94 km to the east of Makkah, overlooking 'Arafah. For those coming from Yemen, he specified Yalamlam (a
                mountain 54 km to the south of Makkah. These places are for the people (coming from the above specified
                countries) as well as for others, who pass by them on their way to perform Hajj or ’Umrah. Those living
                within those boundaries can assume Ihram from where they set out (for the journey), and even the
                residents
                of Makkah, their Miqat would be the place where they are staying in Makkah.’ Agreed upon.
            </div>

            <div class="mainheading topmargin30">
                <h5>RULES FOR IHRAM:</h5>
            </div>
            <div class="infotxt">
                <ul>
                    <li>Do not fight or quarrel with others.</li>
                    <li>Do not curse, talk sinfully or use any bad language.</li>
                    <li>Do not use perfume. This includes scented soaps, shampoos, wipes, lotions etc.</li>
                    <li>Do not cut, pluck or remove any hair or nails. (Broken nails can be removed).</li>
                    <li>Do not engage in any sexual activity.</li>
                    <li>Do not kill, hunt or help others hunt any animals.</li>
                </ul>
            </div>

            <div class="mainheading topmargin30">
                <h5>SECOND PILLAR TAWAF</h5>
            </div>
            <div class="infotxt">
                Circling the Ka'bah seven times in an anticlockwise direction. Men are encouraged to do this three times
                at
                a hurried pace, followed by four times, more closely, at a leisurely pace.<br>
                <br>
                Men should wear their Ihrams so that their right shoulder is bare. Men need to cover their shoulder
                again
                after their Tawaf is complete (i.e. after seven rounds).
            </div>

            <div class="mainheading topmargin30">
                <h5>HOW TO PERFORM TAWAF?</h5>
            </div>
            <div class="infotxt">
                <ul>
                    <li>Begin at the Black Stone. Recite&nbsp;(Bism –ALLAH wa ALLAHU AKBAR)</li>
                    <li>You may touch the Black Stone or point to it without by your hand.</li>
                    <li>Move from the Black Stone to the Yamaani corner. Do not touch any other part of the Ka’bah. You
                        may
                        make any du’a you choose.</li>
                    <li>Once at the Yamaani corner, you can touch it if you are close enough.</li>
                    <li>Continue with your tawaf, circling back towards the Black Stone.</li>
                    <li>Your first tawaf ends where you began, at the Black Stone. Now continue into your second tawaf,
                        and
                        repeat the same steps.</li>
                    <li>You must circle the Ka’bah seven times for your whole Tawaf to be complete.</li>
                    <li>For the first three tawafs,&nbsp;men only&nbsp;should slowly jog. This is called&nbsp;Ramal.
                    </li>
                    <li>Women must walk normally in all seven tawafs. Men should walk normally in the last four tawafs.
                    </li>
                </ul>
            </div>

            <div class="mainheading topmargin30">
                <h5>AFTER TAWAF:</h5>
            </div>
            <div class="infotxt">
                <ul>
                    <li>Go towards&nbsp;<strong>Maqam Ibrahim</strong></li>
                    <li>Stand so&nbsp;&nbsp;<strong>Maqam Ibrahim</strong>&nbsp;is between you and the Ka’bah. Don’t try
                        to
                        stand directly in front of it - it will be far too crowded. Anywhere in Al-Masjid Al-Haram is
                        fine,
                        as long as the Maqam is between you and the Ka’bah.</li>
                    <li>Pray two rakats. It is recommended to recite Surah Kafiroon in the first rakat and Surah Ikhlas
                        in
                        the second rakat (after Surah Fatihah, of course).</li>
                    <li><strong>What must you do after praying at Maqam Ibrahim?</strong></li>
                    <li>Go to any Zamzam water point..</li>
                    <li>Drink Zamzam water while standing. Pouring the water over your head is also recommended.</li>
                    <li>Now you need to leave Al-Masjid Al-Haram for Sa’ee -</li>
                </ul>
            </div>

            <div class="mainheading topmargin30">
                <h5>THE THIRD PILLAR SA'I&nbsp;,</h5>
            </div>
            <div class="infotxt">
                which means rapidly walking seven times back and forth between the hills of&nbsp;Safa and Marwah. This
                is a
                re-enactment of Hajar's frantic search for water. The baby Ismael cried and hit the ground with his foot
                (the story say that an&nbsp;angel&nbsp;scraped his foot or the tip of his wing along the ground), and
                water
                miraculously sprang forth. This source of water is today called the&nbsp;Well of Zamzam.<br>
                <br>
                <ul>
                    <li>It is recommended to have&nbsp;wudu, but it is not compulsory.</li>
                    <li>On your way to Sa’ee, you may recite this&nbsp;ayah about Safa and Marwah.</li>
                    <li>Begin Sa’ee at Safa.</li>
                    <li>You must make seven rounds between the hills of Safa and Marwah. Walking from Safa to Marwa is
                        counted as one round, and Marwah to Safa as another round.</li>
                    <li>Men only&nbsp;must run part of the way. This area is clearly marked by green tubelights.</li>
                    <li>At the top of each hill,&nbsp;recite&nbsp;the du’a&nbsp;(similar to the Eid takbeer)&nbsp;three
                        times. You can make personal du’as after this.</li>
                    <li>And that’s it! Sa’ee is completed after seven rounds, so you will end at Marwah.</li>
                </ul>
            </div>

            <div class="mainheading topmargin30">
                <h5>The fourth HALQ&nbsp;OR&nbsp;TAQSIR:</h5>
            </div>
            <div class="infotxt">
                Taqsir is a partial shortening of the hair typically reserved for women who cut a minimum of one inch or
                more of their hair. A halq is a complete&nbsp;shave of the head, usually performed on men. Both of these
                signify the submission of will to God over glorifying physical appearances. The head shaving/cutting is
                reserved until the end of Umrah.<br>
                <br>
                These rituals complete the Umrah.<br>
                <br>
                The peak times of pilgrimage are the days before, during and after the Hajj and during the last ten days
                of&nbsp;Ramadan.
            </div>

