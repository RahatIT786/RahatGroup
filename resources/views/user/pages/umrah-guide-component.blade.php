                        <h5>THE MEANING OF UMRAH:</h5>
                    </div>
                    <div class="infotxt">The&nbsp;Umrah&nbsp;is an&nbsp;Islamic
                        pilgrimage&nbsp;to&nbsp;Mecca&nbsp;(Hijaz)&nbsp;Saudi Arabia, performed
                        by&nbsp;Muslims&nbsp;that
                        can be undertaken at any time of the year, in contrast to the&nbsp;Hajj&nbsp;which has specific
                        dates according to the&nbsp;Islamic lunar calendar.</div>

                    <div class="mainheading topmargin30">
                        <h5>TYPES OF UMRAH:</h5>
                    </div>
                    <div class="infotxt">
                        A certain type of the Umrah exists depending on whether or not the pilgrim wishes to perform
                        Umrah
                        in the Hajj period, thus combining their merit.<br>
                        <br>
                        When performed alongside the Hajj, Umrah is deemed one of “enjoyment” (Umrat al-tamattu) and is
                        part
                        of a fuller Hajj of enjoyment (Hajjul tamattu). More precisely, the rituals of the Umrah are
                        performed first, and then the Hajj rituals are performed.<br>
                        <br>
                        Otherwise, when performed without continuing to perform Hajj, the Umrah is considered a “single”
                        Umrah (Umrah Mufradah).
                    </div>
