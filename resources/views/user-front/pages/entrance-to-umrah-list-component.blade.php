                                                        (Tawaf):</strong></p>
                                                <ul>
                                                    <li>He/she goes to the Haram</li>
                                                    <li>When the Kaaba is to the left, they circumambulate
                                                        (counterclockwise)</li>
                                                    <li>He begins and ends at the Black Stone</li>
                                                    <li>After completion of the seven rounds, you pray two Rakaah in an
                                                        appropriate place</li>
                                                </ul>
                                                <p class="sc-2fe98234-7 iebARE"><strong>Sa’i (between Safa and
                                                        Marwa):</strong></p>
                                                <p class="sc-2fe98234-7 iebARE">He/she heads to Safa where he starts
                                                    Sa’i towards Marwa. Once reaching Marwa, you have completed one
                                                    round. Crossing the distance back to Safa fulfills the second round,
                                                    and so on until the seventh round ends at Marwa.</p>
                                                <p class="sc-2fe98234-7 iebARE"><strong>Having hair of the head shaven
                                                        or trimmed:</strong></p>
                                                <ul>
                                                    <li>After completing Sa’i, men go to the barber shops to have their
                                                        hair shaven or trimmed</li>
                                                    <li>Women gather their hair and cut from its ends about (1-2 cm)
                                                    </li>
                                                    <li>With this act, the Umrah would be completed, marking the end of
                                                        Ihram</li>
                                                </ul>
                                                <p class="sc-2fe98234-7 iebARE">Umrah pilgrims should avoid any conduct
                                                    that harms others in the Haram, such as jostling, entering crowded
                                                    places, and reversing the direction of circumambulation. They shall
                                                    carefully follow the instructions of observers and security
                                                    personnel.</p>
