                                                    entering the state of Ihram, circumambulating the House, running
                                                    between Safa and Marwa, and having the head shaved or trimmed.</p>
                                                <p class="sc-2fe98234-7 iebARE"><strong>How do I enter into the state
                                                        of Ihram?</strong></p>
                                                <ul>
                                                    <li>A Muslim enters into the state of Ihram at the place specified
                                                        by Sharia, and shall abstain from the Ihram prohibitions</li>
                                                    <li>He shall recite Talbiyah, saying: "Labbayka Allaahumma Labbayk,
                                                        Labbayka La Shareeka Laka Labbayk. Innal Hamda Wan-ni'mata Laka
                                                        Wal-Mulk, La Shareeka Lak" (Here I am at your service, o Allah,
                                                        at Your service! At Your service! You have no partner! I am at
                                                        Your service! Indeed, all praise and grace belong to You, and so
                                                        does the supreme authority. You have no partner)</li>
                                                </ul>
