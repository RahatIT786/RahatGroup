                                                <p class="sc-2fe98234-7 iebARE">The circumambulation (Tawaf) is one
                                                    of the pillars of Umrah and is an act of worship in which, by
                                                    directing our hearts and bodies to Allah, we are humbled confess
                                                    our needs to the Almighty. In this act of worship, the servant
                                                    circumambulates the House of Allah (the Kaaba), worshipping the
                                                    Lord of the House and drawing close to Him as He has prescribed.
                                                </p>
                                                <p class="sc-2fe98234-7 iebARE"><strong>Description of
                                                        Circumambulation</strong></p>
                                                <ul>
                                                    <li>Pilgrims performing Hajj or Umrah start circumambulation
                                                        from the corner where the Black Stone is located. On the
                                                        upper floors, a green sign indicates this position.</li>
                                                    <li>Pilgrims performing Hajj or Umrah should say Takbir in each
                                                        round when they are parallel to the Black Stone, but it is
                                                        not a condition that Takbir should be uttered exactly at
                                                        this point. It is not the case that it is not valid to say
                                                        Takbir before or after it; rather one can do Takbir
                                                        somewhere near this point. So, if the pilgrim sees that he
                                                        has already passed the Black Stone, he can still utter
                                                        Takbir.</li>
                                                    <li>When uttering Takbir, a pilgrim shall point with their hand
                                                        at the Black Stone, and then start circumambulating with the
                                                        Kaaba to his left (counterclockwise).</li>
                                                    <li>Kissing the Black Stone is a Sunnah for those who are able
                                                        to do so, but this is difficult during the Hajj season
                                                        crowded days.</li>
                                                    <li>One continues circumambulation, supplicating Allah and
                                                        praying humbly to Him.</li>
                                                    <li>If one reaches the Yemeni corner, which precedes the Black
                                                        Stone, it is desirable to wipe over it if possible. During
                                                        the Hajj season, this will be difficult.</li>
                                                    <li>The round ends at the corner of the Black Stone, which marks
                                                        the start place. Here, he/she points with their hand and
                                                        utters Takbir.</li>
                                                    <li>One repeats this until completing seven rounds.</li>
                                                </ul>
                                                <p class="sc-2fe98234-7 iebARE"><strong>Circumambulation
                                                        Instructions</strong></p>
                                                <p class="sc-2fe98234-7 iebARE">For a safe and Ideal
                                                    circumambulation:</p>
                                                <ul>
                                                    <li>Walk on without stopping in the circumambulation courtyard
                                                        to prevent crowding and jostling</li>
                                                    <li>Do not reverse the direction of circumambulation, whatever
                                                        the reasons</li>
                                                    <li>The phrase Allahu Akbar (Allah is the greatest) inscribed on
                                                        the Kaaba cloth over the Black Stone indicates the corner of
                                                        the Black Stone</li>
                                                    <li>A green light sign in the upper floor circumambulation
                                                        tracks indicates the alignment of the Black Stone</li>
                                                </ul>
                                                <p class="sc-2fe98234-7 iebARE">Visitors performing supererogatory
